    <br>
    <br>
    <br>
 Nunca em minha vida sonhei em ser mãe, mas as vezes ela gosta de nos dar uns tapas na cara e foi exatamente isso que aconteceu no meu aniversário de dezessete anos. Olhando para aquele teste não conseguia compreender, como tinha sido tão irresponsável, como não havia previsto que aquilo aconteceria. 
    <br>
    <br>
Meus olhos começaram a se encher d'água, minhas mãos tremiam. Eu estava ferrada, já conseguia imaginar a decepção de meus pais, o jeito que me olhariam quando soubessem disso. Foi uma noite, apenas uma noite capaz de mudar tudo, o efêmero bater de asas de uma borboleta. 
<br>
<br>
Lavei meu rosto com água corrente, tentando disfarçar os vestígios de um choro sofrido, os olhos vermelhos me encaravam do outro lado do espelho, culpando-me silenciosamente por meus erros, pelas besteiras que cometemos por pequenos momentos de prazer, para sermos igual a outras pessoas, se encaixar em um padrão que a sociedade impõe. 
<br>
<br>
Girei a maçaneta, a sacolinha preta passando e constantemente me atormentando, passei silenciosamente pelo quarto de meus pais, não queria os acordar, olhar em seus olhos e derramar tudo o que havia feito, ver o desgosto por sua filha que tantas vezes alertaram, cometendo o erro que mais tinham medo que cometesse. 
<br>
<br>
O portão se abriu com um rangido, parecia o grasnar de um corvo aterrorizante. O lixo estava amontoado na esquina, me abaixei rente a um saco grande preto, com dificuldade desfiz o nó e joguei a sacolinha lá dentro. Levantei-me com um pulo apoia terminar todo o processo, entrei novamente para dentro indo até a cozinha, tinha de começar o café da manhã. 
<br>
<br>
Alguns minutos depois ouvi os passos pesados de meu pai no corredor, ele era um homem rígido, devoto a sua fé. Acreditava que a castidade antes do casamento era o pilar de uma vida virtuosa para a mulher. Sempre soube disso, e sempre possui o medo de falhar com ele. 
<br>
<br>
Sentia como se tivesse cometido o maior erro aos olhos de meu pai, e realmente tinha. Como ele poderia aceitar isso? Como eu poderia olhar para ele, sabendo que o tinha decepcionado a imagem que possuía de mim? Sentiu a bile subir por minha garganta, a tontura nublando momentaneamente meus sentidos. Apoie-me no batente da pia para não cair no chão. 
<br>
<br>
Tomando forças, me livrei de meu suporte. Precisava conversar com alguém, não aguentaria guardar esse segredo apenas comigo, aproveitei enquanto meu pai estava no banheiro e adentrei o quarto que ele compartilhava com minha mãe. Ela estava sentada na penteadeira arrumando seus cabelos, fazia parte de seu ritual da manhã: 
<br>
<br>
-M-mãe. - Minha voz estava embriagada pelo choro, estava hiperventilado, não conseguia respirar. 
<br>
<br>
-Mariana, o que aconteceu? - Perguntou se levantando, sua voz suave atiçando ainda mais meus nervos. 
<br>
<br>
Não conseguia responder, tinha um nó em minha garganta que teimava em sair matando-me lentamente na angustia. Minha mãe se aproximou, me conhecia perfeitamente, conseguia ver tudo em meu rosto, aproximou lentamente sua mão de minha testa: 
<br>
<br>
-Você está queimando querida. - Disse suavemente, sem esquecer a apreensão. - Você não está bem, meu amor. Vamos ao hospital. 
<br>
<br>
Não conseguia resistir mais. Desabei a chorar nos braços de minha mãe, não conseguia mais manter as emoções sob controle. Meu corpo tremia, as palavras saindo em um lamento desesperado de meus lábios: 
<br>
<br>
-Mãe, eu estou gravida. 
<br>
<br>
Senti seus braços se desvencilhando de minhas costas, ela se afastou lentamente, não possuía a coragem necessária para olhar em seu rosto, ver a surpresa, o desgosto a decepção. Sabia que havia cometido um grave erro, me deixei influenciar por pessoas, afundei-me em algo que lentamente me devorava. 
<br>
<br>
Se não tivesse ido aquela festa, não tivesse bebido e me deixado influenciar por um cara qualquer com cantadas bestas. O cheiro de fumo no ar, as risadas altas e todas as luzes brilhantes. Suas mãos levantaram meu rosto: 
<br>
<br>
-O que? Mariana, não... isso não é possível. Você tem certeza? - Assenti com a cabeça, ainda entre soluços. 
<br>
<br>
-Eu fiz o teste. Ele deu positivo. - Um soluço me deixou. - Eu... não sei o que fazer, mãe. Não sei o que fazer! 
<br>
<br>
Ela estava quieta, seus olhos cheios de incertezas. Sentou-se ao meu lado na cama, puxando-me para um abraço apertado. Parecia sentir a dor que eu sentia, mas sabia que o que mais a preocupava era quando meu pai, Carlos, soubesse da notícia. Meu pai sempre firme para suas crenças religiosas. 
<br>
<br>
-Você está me dizendo... o que vamos fazer? - Perguntou-me, a voz quebrada, mas ainda tentando manter a calma por nos duas. - Mariana, você sabe o que isso significa, não sabe? 
<br>
<br>
Não, eu não sabia como era estar gravida, gravida em uma casa extremamente religiosa, onde ao mais mínimo signo de pecado seu pai surtaria. E agora aqui eu estava esperando uma criança de alguém que não conheço. Estou em pânico. 
<br>
<br>
-Eu não poso contar para ele, mãe. Não posso. Ele... ele vai me odiar. Ele nunca vai me perdoar. Não quero ver essa decepção no rosto del. Não posso, não posso. - Não reconhecia, mas minha própria voz, nublada pelo desespero de que meu pai chegasse a saber da notícia. 
<br>
<br>
Minha mãe respirou fundo, o peso invisível da responsabilidade pairando sobre seus ombros. Entendia que não seria fácil. Sei que a reação de meu pai seria dura, cruel, mas entendia que lá dentro no fundo de sua alma ele me amava. Mas como contar a verdade sem destrui-lo? 
<br>
<br>
-Mariana, minha filha, você precisa ser honesta com ele. Ele vai perceber, daqui uns meses nada poderá esconder sua barriga. - Começou olhando no fundo dos meus olhos. - Ele vai ter que saber. E nós vamos passar por isso juntas, pois apesar de tudo você ainda é minha pequena princesinha. 
<br>
<br>
Não conseguia acreditar nas palavras. Temia fortemente a reação de meu pa. Mas sabia lá no fundo de meu inconsciente que minha mãe estava certa. Eu precisava enfrentar isso, e não estaria sozinha nessa jornada, a teria me acompanhando e ajudando. Não havia mais escapatória. 
<br>
<br>
Ouvimos quando meu pai se despediu para ir trabalhar, uma ansiedade começou a tomar conta de meu peito, teria a te o fim da tarde para pensar em como dize-lo a grande notícia. Algum tempo de pois, minha mãe mandou-me tomar um banho e descansar um pouco enquanto ia fazer os preparativos para o dia. 
<br>
<br>
Horas mais tarde, ouvi o carro estaciona na garagem, sentia o ambiente, estava sentada no sofá, o coração acelerado a coberta o único conforto que tinha naquele momento, minha mãe sentada ao meu lado dando seu silencioso apoio. Seus passos estavam cada vez mais pertos da sala, quando ele entrou seu olhar foi em direção ao meu. 
<br>
<br>
-Carlos, precisamos conversar. - Disse minha mãe, sua voz nunca saindo de seu timbre suave, mas firme. - Mariana... Mariana tem algo importante para te contar. 
<br>
<br>
Seu olhar voltou para o meu, grave e severo: - O que está acontecendo, Mariana? 
<br>
<br>
Sentia as palavras se amontoando em minha garganta, lutando para permanecerem presas, minha mãe apertou minhas mãos em um claro sinal de conforto: - Pai... eu... estou gravida. - Um sussurro. 
<br>
<br>
A expressão em seu rosto congelou, senti como se uma geada tivesse se apossado de meu corpo. Mas rapidamente a raiva tomou conta de sua expressão, olhava-me como se não acreditasse em seus ouvidos. Como ele poderia? Como ele conseguiria me olhar depois de tudo que me ensinara sobre pureza e castidade? 
<br>
<br>
-Você... você está gravida? - Repetiu, quase sem acreditar, sua voz tremendo de fúria e tristeza. 
<br>
<br>
Fechei meus olhos, sentindo a vergonha e medo me consumindo. Sabia que inevitavelmente sofreria uma punição. Meu pai nunca me perdoaria: -Eu... eu não queria, pai. Eu não queria sou apenas uma criança. Eu não sei o que fazer. Eu tenho medo de como isso vai mudar tudo. - Voz embriagada pelo choro. 
<br>
<br>
Meu pai respirou fundo tentando controlar a raiva. Ele parecia estar tentando encontrar uma resposta, uma solução, mas no fundo sabia que nada mais seria como antes. Ele sempre acreditou em mim, sua única filha, acreditava que eu deveria seguir as regras, que deveria ser um exemplo de retidão. E agora aqui estava eu, preste a destruir tudo o que ele acreditava. 
<br>
<br>
-Você tem noção do que fez, Mariana? Você comprometeu sua vida, sua moral. Como pode fazer isso? Como irei olhar tua face, sabendo que... que você falhou conosco? 
<br>
<br>
Senti meu corpo inteiro tremer, minha mãe me puxando para seus braços. Não sabia como responder a seu questionamento. A dor alucinante tomando conta de meu coração e alma, a dor de ser jugada por uma das pessoas que eu mais amava e respeitava, estava me consumindo por dentro. 
<br>
<br>
Mas, em meio a essa dor, sabia também que algo havia mudado. Não poderia voltar atrás. Minha gravidez era real. O medo da rejeição de meu pai persistia em meus ossos, mas a vida seguiria de alguma forma, com ou sem a aprovação de meu pai. Aprenderia a lidar e conviver com aquilo, apenas uma coisa me dava uma extrema certeza: Não estava, mas sozinha, tenho e terei minha mãe. E juntas encontraríamos forças para seguir. 
