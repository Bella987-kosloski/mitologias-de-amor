    Pele parda e radiante, uma verdadeira obra de beleza refinada. Seu rosto delicado, com feições suaves, exibindo um tom moreno dourado que reflete a luz com uma cativante suavidade. Os cabelos negros como a noite, sedosos caindo em cascatas sobre seus ombros, contrastando impecavelmente com a tonalidade quente de sua pele. Seus olhos, um âmbar profundo, brilhando com uma intensidade suave, escondendo histórias e segredos em seu penetrante olhar. 
    <br>
    <br>
    As sobrancelhas perfeitamente desenhadas e o contorno dos lábios um sutil tom de rosa complementando sua feição etérea e misteriosa. A boneca estava vestida com um traje elegante, adornada com detalhes em renda e cetim destacando sua aparência sofisticada e encantadora. 
    <br>
    <br>
   Cada detalhe, desde a leve curvatura de seus dedos até o brilho sutil de sua pele, confere-lhe uma presença imponente, como se fosse uma peça rara e preciosa, esperando para contar sua própria história encantada. 
   <br>
   <br>
   Sua cintura envolta pelos dedos enrugados de Dona Clara, segurando-a no ar diante de meus olhos, permitindo-me observar sua encantadora beleza. Já vira diversas bonecas, mesmo não sendo muito fã, mas nenhuma era tão bela quanto essa, me hipnotizando lentamente: 
   <br>
   <br>
   - Sei que nunca foste uma para as bonecas. - Começou, sua voz envelhecida. - Mas essa não é uma boneca qualquer. É algo muito especial. 
   <br>
   <br>
   Estranhamente acreditava nela, estava ansiosa para ter a pequena boneca de porcelana em meus braços, saber se seus cabelos eram realmente sedosos. Estendi minhas mãos, dona Clara a depositou cuidadosamente em minhas palmas abertas. 
   <br>
   <br>
   - Muito obrigado. -  Apenas um sussurro, sempre fui uma criança muito tímida. 
   <br>
   <br>
   Dona Clara sorriu, um sorriso que alcançava seus olhos. Ela era uma síndica muito boazinha. Não duvidaria nada que tenha regalado uma para cada menina da comunidade.? 
   <br>
   <br>
   - Ela é um presente muito especial, Sofia. Quero que você cuide dela com muito carinho. 
   <br>
   <br>
   Apertei a boneca em meu peito, assentindo lentamente com a cabeça. Dona Clara sorriu mais uma vez: 
   <br>
   <br>
   - Irei cuidar muito bem dela. - Esperava que meus olhos transmitissem toda a confiança e alegria. 
   <br>
   <br>
   Dona Clara se abaixou deixando um beijo em minha testa. Depois disso se despediu de meus pais, indo para a casa ao lado, percebi pela janela que em suas mãos agora repousava uma pequena caixa. Sabia que dentro dela estava mais bonecas, mas nenhuma seria tão especial quanto a minha. 
   <br>
   <br>
   Com a boneca ainda em minhas mãos, me levantei do chão, querendo ir em direção do quarto, foi nesse momento que Bruno, meu irmãozinho de quatro anos saiu do quarto, seu rosto amassado pelo sono. Inconscientemente apertei mais firmemente a boneca em meu peito. 
   <br>
   <br>
   Amava imensamente eu irmão, mas tinha de admitir que ele era um pequeno mimado. Todos os brinquedos que possuía tinha que dividir com ele, e todos ele acabava destruindo, enquanto os seus ficavam intactos. E quando queria brincar com um de seus carrinhos fazia um aue. 
   <br>
   <br>
   Não queria que ele a quebrasse. Como se lesse meus pensamentos seus olhos voltaram-se para a boneca em minhas mãos, brilhando intensamente. Com passos apresados tentou se aproximar, mas fui rápida em me esquivar. 
   <br>
   <br>
   Seus lábios começaram a tremer, eu sabia o que vinha pela frente: - Me dá!!! -  Começou a gritar tentando pegá-la de minha mão, a cada momento desviava, ela era minha ele não tem o direito de destrui-la como fez com todos os outros brinquedos. 
   <br>
   <br>
   Seus olhos começaram a lagrimar e um beicinho surgiu em sua boca, “Merda!”. Apenas pensei e ele desatou a chorar, ouvi quando minha mãe largou as panelas e veio ver o que estava acontecendo, seus passos ecoando pelo pequeno corredor. 
   <br>
   <br>
   Quando chegou a sala, foi direto a meu irmão secando as lagrimas que desciam por sua bochecha: - Theo, o que houve? – Ele lançou um olhar para mim de onde estava escondido nos braços de nossa mãe. 
   <br>
   <br>
   - A sofi não que me dar a boneca.- Soluços fingidos deixavam seus lábios, minha mãe me olhou com um olhar repleto de repreensão. 
   <br>
   <br>
   - Deixe seu irmão brincar um pouco com a boneca.  Ele não irá estragar. -Ela disse, mesmo assim recusei segurando-a mais fortemente contra mim 
   <br>
   <br>
   - Mãeeeee. Disse Theo manhoso percebendo que não cederia. Vendo que não iria dar a boneca um pouco para ele, ela começou a me repreender. Estava tão focada em minha mãe que não percebi a hora que Theo se aproximou. Vi em câmera lenta como ele batia na boneca e ela voava de minha mão de encontro ao chão, ouvi o ‘crack’ da porcelana quebrando. 
   
   Meus olhos se encheram de lagrimas, seu rosto estava despedaçado, seus cabelos espalhados e o vestido de seda completamente destruído. Agachei pegando delicadamente os pedaços da porcelana. As lagrimas corriam livremente por minhas bochechas, minha visão estava embaçada. 
   <br>
   <br>
   Terminando de juntar todos os pedaços sai em disparada para o meu quarto, tranquei-me nele, não me importando quando minha mãe começou a bater na porta me pedindo para sair. Só conseguia chorar, chorar por algo que me tinha sido arrebatado. 

