    <br>
    <br>
    
    O som do esfregam molhado entorpecia meus sentidos, os calos em minhas mãos ardiam pelo constante movimento que fazia. Estava fedendo a suor e produtos de limpeza, sendo o mais intenso o cheiro da Qboa. 
    <br>
    <br>
    Fazia alguns meses que vim trabalhar no Teatro municipal do Rio de Janeiro, digamos que quando você tem o grande sonho de fazer faculdade, se mudar para outro estado sem uma renda, não é uma das coisas mais inteligentes a se fazer. Mas após muitos perrengues, corre para lá, corre pra cá, finalmente achei um emprego de zelador no Teatro. 
    <br>
    <br>
    Nunca fui uma pessoa muito ligada a arte erudita, sinceramente prefiro as famosas ‘modinhas’ como dizem os mais velhos, mas depois de um tempo trabalhando aqui peguei um pequeno apego com as melodias de violinos e pianos. 
    <br>
    <br>
    Empurrei as grandes portas de madeira que davam lugar a arquibancada retrátil, quase todas as cadeiras estavam em seus devidos lugares. A algumas horas teve um concerto, que consequentemente deixou o teatro um pouco bagunçado. 
    <br>
    <br>
    Abaixei o balde deixando um pouco de água cair no chão, antes de começar a fazer qualquer coisa precisava fechar os acentos que ainda estavam abertos. Foi somente quando levantei meus olhos que travei no lugar, sem conseguir formular nenhum pensamento. 
    <br>
    <br>
    No palco envolto pela luz dos refletores, um bailarino emerge, sua pele morena reluzindo em seu collant com pequenas pedras presas, seus lábios estavam entreabertos, ele parecia quase etéreo. Sua aparência lembrando-me dos belos quadros que estavam espaceados pelos corredores do Teatro. 
    <br>
    <br>
    Como uma deixa a sinfonia começou a tocar, o violino com um triste lamento. Acompanhando seus acordes os primeiros passos são lentos e pesados, como se cada movimento que fazia exigisse um tremendo esforço. A música uma lamuria suave, uma melodia que parecia chorar junto dele. Seus olhos estão baixos, e sua face refletindo uma profunda tristeza. Como se carregasse o fardo do mundo que o rejeita. 
    <br>
    <br>
    Seus braços moviam-se com uma graça triste, um gesto em uma busca desesperada, não era uma celebração. Respiração controlada, se contorcendo como se quisesse escapar de um invisível fardo, cada giro revelando uma fragilidade angustiante, tocando profundamente meu coração. Sou apenas um telespectador de seu sofrimento.  
    <br>
    <br>
    Quando ele se ergueu, as sapatilhas quase não faziam barulho contra o chão, pareceu tentar alcançar algo além do alcançável, mas logo essa energia se esvai, e ele cai de joelhos, a cabeça baixa, um gesto de rendição. Segurei o cabo do esfregão, as gotículas molhando o chão dentre os acentos. Meus olhos perseguiam o belo dançarino como o relógio persegue a hora, minha mente se deixando levar pela angústia que aquela dança transmitia. 
    <br>
    <br>
    Era o sofrimento de um pássaro preso a uma gaiola, condenado a eternamente entreter as pessoas com seus lamentosos cantos. Os passos ficaram mais angustiados, como se estivesse se debatendo entre a beleza da dança e a dor que o consome. Cada passo parecia uma tentativa de se libertar, mas a gravidade o puxava para baixo, simbolizando um desespero sem fim. Minha garganta fechando enquanto via a bela performance. 
    <br>
    <br>
    A dança ficou mais desesperada, seus passos tremendo pelo palco, a sinfonia agitada em dor.  No clímax da apresentação, ele se ergue novamente, mas agora com os olhos cheios de lagrimas. O corpo tremulo, e os braços estendidos em um apelo silencioso. Precisava se libertar, soltar as amarras que o prendiam tão fortemente, voar novamente no céu sentir sua liberdade, o bater dos ventos em seu rosto. Sem perceber lagrimas caíram de meu rosto, como se quisessem consolar a pessoa que tão angustiadamente dançava. 
    <br>
    <br>
    Então, em um momento de vulnerabilidade crua, ele se deixa cair no chão, o corpo curvado, como se o peso da tristeza finalmente o tivesse vencido. A luz parecia se apagar, e ele permanecia ali, em uma posição de entrega e tristeza profunda, enquanto o eco da música ressoa, deixando uma sensação de perda e desamparo no ar. 
    <br>
    <br>
     O esfregão caiu de minha mão enquanto um soluço deixava meus lábios. Neste momento entendi o que a muitos anos meu professor de artes havia dito em sala “Quando um bailarino dança, dependendo da dança você sentira sua alma dançar junto a angústia dele.”, nunca acreditei nessa frase, achei que era apenas uma bobagem, mas agora não conseguia parar minhas lamurias. 
     <br>
     <br>
    Um baque surdo no chão, meu corpo deslizou pelo encosto da cadeira, enquanto minhas mãos tentavam conter meus soluços. Queria levantar, correr até o palco, e abraçar aquele homem que tão tristemente dançava. Uma dança que conseguiu me transmitir a tristeza de uma alma quebrantada. 
    <br>
    <br>
    Tão perdido estava em tentar controlar meus soluços, que quase dei um pulo de susto quando braços me rodeavam. O cheiro de suor misturado com o característico do kaiak preenchendo minhas narinas, podiam sentir mãos fazendo carinho em minha coluna e cabelos. Tentando de alguma forma me confortar, aos poucos meus soluços viraram lamurias suaves, o aperto diminuindo. Falanges delicadas secando as lagrimas que desciam como cascata. Me encontrei com seus olhos que me olhavam com carinho. 
    <br>
    <br>
    Minha respiração se regularizou, mas ainda não conseguia me levantar, me prendi veementemente ao seu tórax, no podia o deixar, não nesse momento em que ainda estava abrumado pela avalanche de emoções angustiantes:-Você está bem? - Sua voz era baixa, suave, como se tivesse medo de me assustar. Não assustaria, e ele não deveria me dar conforto, tinha de ser ao contrário, era ele que a pouco se entregava a uma dor tremenda em sua dança. 
    <br>
    <br>
    Contraria a minha vontade assenti com a cabeça, como o diria que me encontrava naquele estado por vê-lo dançar, que sua performasse tinha grudado tão profundamente em m’alma que nunca poderia esquecê-la, mesmo se fechasse os olhos, ainda veria os passos que apenas transmitiam tristezas.  A forma como seus olhos refletiam a dor de não conseguir mais carregar um fardo. 
    <br>
    <br>
    Um sorriso enfeitou sua face, seus olhos castanhos alegres. Possuía a leve impressão de que minha face deixava transparecer todos meus pensamentos. Sua mão arrulhou meu rosto, minha bochecha descansando em sua palma aberta. Ele não comentou nada apenas deixou-me descansar contra si, enquanto me recompunha. 
    <br>
    <br>
    Pareceu se passar horas até que me senti confiante em lhe soltar. Meus braços se desvencilharam de seu corpo, e meu rosto de sua palma. Notei que ele ainda estava um pouco ofegante pela sua performasse, senti uma pontada de culpa por o prender aqui, quando ele poderia estar terminando de se trocar para ir embora. 
    <br>
    <br>
     Sabia por algumas amigas que já fizeram ballet o quão desconfortável era usar essas sapatilhas. Uma dor alucinante: - Desculpa. - Saiu em um sussurro, seus olhos confusos voltados para mim. 
     <br>
     <br>
    -Pelo que se desculpa? - Sua voz continuava baixa. - Você não fez nada de errado. 
    <br>
    <br>
    Queria o dizer que sim, que por causa de meu pequeno e emocionante coração, que nunca tinha visto algo tão belo, impedi-o de se livrar das amaras que eram as sapatilhas, de descansar depois de uma tão intensa performance. 
    <br>
    <br>
    -Você dança magnificamente bem. - No final de tudo o que queria falar, aquilo foi a única coisa que deixou meus lábios. 
    <br>
    <br>
    Ele soltou uma pequena risada: -Muito obrigado. 
    <br>
    <br>
    Nunca fui um para as artes clássicas, mas se esse belo bailarino dançasse novamente. Daria até meu último centavo para poder vê-lo. 

