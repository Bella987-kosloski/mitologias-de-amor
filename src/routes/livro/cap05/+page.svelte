<br><br><br>

<p>O forte vento batia em meu rosto, enquanto retornava para casa, era uma tarde tempestuosa, apesar de momentos atrás o sol brilhar radiantemente no céu. Acho que essa é uma das desvantagens de morar em um país tropical.</p>

<br><br>

<p>Ao meu lado, Tabhata colocava sua tão característica jaqueta, com a mochila rosa aos seus pés, o chaveiro de flor balançando suavemente. Eu queria ter um casaco nesse momento, sou uma frienta de carteirinha. Mas naquela manhã, quando saí para a escola, o sol estava tão alucinantemente quente que nem considerei que poderia chover.</p>

<br><br>

<p>Meus cabelos voaram em meu rosto, estavam gigantes, chegando até meus quadris, mas ainda não tinha virado o ano. Eu tinha uma promessa, e não a descumpriria, não importa o que. Apenas quando o ano virasse, iria até Dona Audria para cortar essa juba.</p>

<br><br>

<p>- Vamos, Bel. - Me chamou, assim que colocou a mochila nas costas. Nem percebi que estava perdida em pensamentos até esse momento. – Não queremos pegar chuva.</p>

<br><br>

<p>Acenei com a cabeça, pegando a chuquinha do meu pulso para prender minhas rebeldes madeixas. Algumas gotas caíram em meu punho, e, ao olhar para o céu, vi que a pequena garoa começava a engrossar. Olhei para Tabhata, ela olhou para mim, e começamos a rir enquanto saíamos correndo.</p>

<br><br>

<p>Para nossa sorte, a casa dela era apenas algumas quadras depois da escola. Na verdade, tudo nessa pequena cidade do interior de São Paulo era perto. A casa da Tabhata ficava três quadras para frente da escola, uma esquina descendo.</p>

<br><br>

<p>Apesar de ser perto, ainda chegamos completamente encharcadas. A casa de Tabhata era bem humilde, com muros baixos, o portãozinho de ferro meio enferrujado e um pé de goiaba que caía suavemente no muro, com alguns frutos já maduros.</p>

<br><br>

<p>Tabhata tinha dois cachorros: uma pinscher chamada Princesa -mesmo que fosse o próprio diabo- e um pitbull chamado Esmagador, que era um amor de pessoa. Entramos pelo portão, e logo fomos recebidas pelo Esmagador, pulando em nossas pernas com a língua para fora.</p>

<br><br>

<p>- Oi, garoto! - Disse, passando a mão por sua cabeça, vendo-o balançar o rabo de alegria.</p>

<br><br>

<p>- Ben! - Ela gritou para o irmãozinho mais novo. Ele ficava sozinho à tarde até Tabhata chegar da escola. A mãe deles era mãe solo, desde que seu marido os abandonou, e não tinha condições de contratar uma babá.</p>

<br><br>

<p>Logo, passos apressados foram ouvidos, e Ben apareceu com duas toalhas na mão. Seus olhos brilharam de alegria quando me viu. Ele amava quando eu visitava, gostava de brincar comigo. Apesar de Tabhata ser uma pessoa muito doce, não tinha muita paciência quando se tratava de seu irmão.</p>

<br><br>

<p>- Oi, pequeno. - O cumprimentei com um beijo na testa, algumas gotas de água caindo de meu cabelo sobre seu rosto. Ele deu uma leve risadinha enquanto jogava a toalha em minha cabeça.</p>

<br><br>

<p>- Oi, Bel. - Seu sorriso é contagiante. - Você parece um pouco encharcada. - Ele disse em deboche, rindo de minha cara.</p>

<br><br>

<p>- Haha, pequeno engraçadinho. - Nesse momento, Tabhata já havia sumido no corredor, com certeza indo para seu quarto pegar roupas novas para nós. Porque sinceramente, a roupa grudada ao meu corpo estava me dando raiva.</p>

<br><br>

<p>Sentei-me no chão da varanda, tirando o tênis. Não existe sensação pior do que a meia molhada, dava uma raiva ouvir o barulho da meia encharcada no pé.</p>

<br><br>

<p>- Aqui, Bel. - Tabhata me entregou um short e uma blusa. - Pode se trocar no banheiro.</p>

<br><br>

<p>Me levantei e fui até o banheiro, tirando as roupas com dificuldade. Outra coisa ruim é que, quando você se molha, suas roupas grudam de um jeito que parece que você passou Super Bonder. Terminei de me trocar e coloquei a roupa molhada dentro de uma sacolinha que sempre deixava na bolsa.</p>

<br><br>

<p>Saí do banheiro e me joguei ao lado do Ben, que assistia Scooby-Doo na sala. Esse era meu desenho favorito, apenas perdendo para Dragon Ball. Meu pai garantiu viciar todos os seus filhos em Naruto e Dragon Ball, para desespero de minha mãe, que odiava esses desenhos.</p>

<br><br>

<p>Fiquei com os dois até a chuva passar. Quando vi que já era seguro voltar para casa, me despedi dos dois com um abraço. O céu já mostrava alguns tons alaranjados. Quando cheguei na esquina, uma sensação de terror se apoderou de meu corpo. Sempre fui uma pessoa que confiava muito nos meus instintos.</p>

<br><br>

<p>Olhei disfarçadamente para trás. Havia duas pessoas acenando para mim. Um homem e uma mulher. Eu sabia quem eram, eu os conhecia, mas parecia que meu cérebro havia entrado em curto-circuito. Comecei a correr como se não houvesse amanhã.</p>

<br><br>

<p>Por trás de mim, meus pais me gritavam desesperadamente, mas não eram meus pais. Eu pulava de calçada em calçada, minha respiração descompassada e meu coração acelerado. Meus joelhos já estavam falhos. Para uma pessoa sedentária como eu, correr toda essa distância era esgotante.</p>

<br><br>

<p>Em certo momento, fiz uma curva complicada. Já não estava mais nas ruas da cidade, o velho trilho de trem rodeado de árvores parecia o cenário de um filme de terror, daqueles que fariam as pessoas terem pesadelos por meses.</p>

<br><br>

<p>Olhei para baixo e vi meus pais me procurando pelas ruas. Como se eu fosse um satélite, podia ver tudo o que acontecia na Terra. Eles viravam esquinas e mais esquinas me procurando, às vezes parando para discutir entre si, em um impasse sem fim.</p>

<br><br>

<p>Meu corpo se arrepiou quando um vento extremamente gelado passou. Precisava ir para casa. Os raios no céu me davam a certeza de que logo começaria a chover novamente.</p>

<br><br>

<p>Virei-me, pronta para deixar aquele trilho, quando, em um bater de asas, sentado em cima de um tronco cortado sobre os trilhos, estava um homem. Mas não sei se posso chamá-lo assim. As grandes asas negras que saíam de sua escápula e se arrastavam até o fim da lateral dos trilhos, fundindo-se à vegetação ao redor. Asas tão negras quanto a noite.</p>

<br><br>

<p>Seus olhos combinavam com seus cabelos, como o abismo, e sua pele de marfim lhe dava uma aparência etérea. Seus lábios rosados, com um suave sorriso, me encaravam como se finalmente tivesse encontrado a razão de sua existência. Ele seguia cada tremor de meu corpo, minha respiração compassada e meus olhos amedrontados.</p>

<br><br>

<p>Ele vestia um terno a jogo com suas asas, apenas um pequeno lenço vermelho em seu bolso quebrava toda aquela escuridão. Suas pernas estavam cruzadas e seu rosto apoiado em uma das mãos, sua postura confortável me fez momentaneamente esquecer das pessoas que me procuravam, gritando meu nome aos quatro ventos.</p>

<br><br>

<p>Sem nem perceber, meus pés se moveram em direção a ele, parando a apenas meio metro de distância. O arrastar de suas asas me hipnotizou, assim como seu sorriso, que ficou um pouco maior. Seus olhos brilhavam de uma maneira que eu não compreendia, mas que me fazia sentir segura, amada, protegida.</p>

<br><br>

<p>Minha garganta estava apertada, não conseguia desviar o olhar das suas obsidianas. Meu coração fazia um ruído surdo em meus ouvidos, e minha respiração estava mais descompassada. Queria me aproximar, deixar-me descansar contra seu torso, enquanto suas asas me envolviam com amor, me protegendo em um casulo onde ninguém poderia me fazer mal.</p>

<br><br>

<p>Eu precisava dar apenas alguns passos. Meus cabelos ainda molhados e agora soltos, voaram com o vento, alguns fios ficando presos em meus lábios. Nesse momento, o belo anjo abriu a boca, ele tinha que ser um anjo.</p>

<br><br>

<p>- Eu lhe protegerei. - Sua voz profunda, com uma rouquidão suave, aqueceu meu coração e enfeitiçou minha mente lentamente. Uma prisão à qual estou disposta a ficar. Sem medo algum.</p>

<br><br>

<p>O mundo ao meu redor parecia rodopiar. Seus olhos nunca deixaram os meus, e seus lábios repetiam a frase enquanto um turbilhão se desenrolava em minha mente, me deixando tonta.</p>

<br><br>

<p>Meus olhos se abriram junto ao cantar dos pássaros. O sol apenas começava a raiar. Estava perfeitamente enrolada na coberta, com lágrimas saindo dos meus olhos, não conseguindo conter os soluços. Sentia uma sensação de proteção, meu coração dolorido pelo belo estranho que jurara me proteger.</p>

<br><br>

<p>Virei-me de lado, trazendo a coberta até meus lábios para abafar os soluços. Devia ter me aproximado, deixado todo o amor que seus olhos me mostravam me acolher, me guardar com ele.</p>

<br><br>

<p>Queria continuar na presença daquele ANJO DE ASAS NEGRAS.</p>

